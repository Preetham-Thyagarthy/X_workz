//Program to display the Array elements using for-each loop
public class Foreach
{
    public static void main(String[]args)
    {
        int[]arr={1, 2, 3, 4, 5, 6, 7, 8 };
        for( int a: arr)    //Each element of arr (array) is stored in the variable
            System.out.println(a);
    }
}
