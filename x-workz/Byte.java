class Byte
{
	public static void main(String[] args)
	{
		byte a=10;
		byte p=20;
		byte s=30;
		byte k=50;
		byte c=15;
		System.out.println("Initinal Values;");
		System.out.println("Age: "+a);
		System.out.println("Pravith: "+p);
		System.out.println("Skanda: "+s);
		System.out.println("keys: "+k);
		System.out.println("car: "+c);
		
		a=5;
		p=15;
		s=25;
		k=45;
		c=4;
		System.out.println("Re-Initinal values: ");
		System.out.println("Age: "+a);
		System.out.println("Pravith: "+p);
		System.out.println("Skanda: "+s);
		System.out.println("Keys: "+k);
		System.out.println("Car: "+c);
	}
}